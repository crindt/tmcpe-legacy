SELECT 
TMCDBA.EQUIPLOOPACTIVE.VDS_ID,
TMCDBA.EQUIPLOOPACTIVE.CAL_POSTMILE,
TMCDBA.EQUIPLOOPACTIVE.DIRECTION,
TMCDBA.FREEWAY.DESIGNATOR,
TMCDBA.FREEWAY.FREEWAY_ID,
TMCDBA.EQUIPLOOPACTIVE.COUNTY,
TMCDBA.EQUIPLOOPACTIVE.CITY,
TMCDBA.EQUIPLOOPACTIVE.LOOP_NAME,
TMCDBA.EQUIPLOOPACTIVE.LANE_INSTALLED,
TMCDBA.VDSCONFIG.PHYSICAL_LANES AS VCPL,
TMCDBA.EQUIPVDSACTIVE.PHYSICAL_LANES AS VAPL,
TMCDBA.VDSRMSCONFIG.NUM_METERED_LANES,
TMCDBA.EQUIPLOOPACTIVE.LOGICAL_POSITION,
TMCDBA.D_LOOPTYPE.LOOP_TYPE,
TMCDBA.D_LOOPTYPE.DESCRIPTION
FROM TMCDBA.EQUIPLOOPACTIVE 
LEFT JOIN TMCDBA.D_LOOPTYPE ON TMCDBA.D_LOOPTYPE.LOOP_TYPE = TMCDBA.EQUIPLOOPACTIVE.LOOP_TYPE
LEFT JOIN TMCDBA.FREEWAY ON TMCDBA.FREEWAY.FREEWAY_ID =  TMCDBA.EQUIPLOOPACTIVE.FREEWAY_ID
LEFT JOIN TMCDBA.VDSCONFIG ON TMCDBA.VDSCONFIG.VDS_ID = TMCDBA.EQUIPLOOPACTIVE.VDS_ID
LEFT JOIN TMCDBA.VDSRMSCONFIG ON TMCDBA.VDSRMSCONFIG.ON_RAMP_VDS_ID = TMCDBA.EQUIPLOOPACTIVE.VDS_ID
LEFT JOIN TMCDBA.EQUIPVDSACTIVE ON TMCDBA.EQUIPVDSACTIVE.VDS_ID = TMCDBA.EQUIPLOOPACTIVE.VDS_ID
WHERE TMCDBA.VDSCONFIG.VDS_TYPE = 'ML'
AND TMCDBA.FREEWAY.FREEWAY_ID = 405
AND ( TMCDBA.EQUIPLOOPACTIVE.DIRECTION = 'N' OR  TMCDBA.EQUIPLOOPACTIVE.DIRECTION = 'S' )
ORDER BY TMCDBA.EQUIPLOOPACTIVE.DIRECTION,TMCDBA.EQUIPLOOPACTIVE.CAL_POSTMILE,TMCDBA.EQUIPLOOPACTIVE.VDS_ID,  TMCDBA.EQUIPLOOPACTIVE.LANE_INSTALLED
;

